void test_display(void) {
	wr32(RAM_DL + 0, CLEAR(1, 1, 1));               // clear screen
	wr32(RAM_DL + 4, BEGIN(BITMAPS));               // start drawing bitmaps
	wr32(RAM_DL + 8, VERTEX2II(220, 110, 31, 'T')); // ascii T in font 31
	wr32(RAM_DL + 12, VERTEX2II(244, 110, 31, 'E')); // ascii E
	wr32(RAM_DL + 16, VERTEX2II(270, 110, 31, 'X')); // ascii X
	wr32(RAM_DL + 20, VERTEX2II(299, 110, 31, 'T')); // ascii T
	wr32(RAM_DL + 24, END());
	wr32(RAM_DL + 28, COLOR_RGB(160, 22, 22)); // change colour to red
	wr32(RAM_DL + 32,
	     POINT_SIZE(320)); // set point size to 20 pixels in radius
	wr32(RAM_DL + 36, BEGIN(POINTS));             // start drawing points
	wr32(RAM_DL + 40, VERTEX2II(192, 133, 0, 0)); // red point
	wr32(RAM_DL + 44, END());
	wr32(RAM_DL + 48, DISPLAY()); // display the image
	wr8(REG_DLSWAP, DLSWAP_FRAME);
}

void readerr(char str[128]) {
	for (int i = 0; i < 128; i++) {
		char c = rd8(RAM_ERR_REPORT + i);
		if (c == '\0')
			return;
		if (str[i] != c) {
			str[i] = c;
		}
	}
}

